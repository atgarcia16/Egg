package ejercicio10.Entidades;

/**
 *
 * @author Atilio Garcia
 */
public class Arreglos {

    private int vA;
    private double vB;

    public Arreglos() {
    }

    public Arreglos(int A, double B) {
        this.vA = A;
        this.vB = B;
    }

    public double getA() {
        return vA;
    }

    public void setA(int A) {
        this.vA = A;
    }

    public double getB() {
        return vB;
    }

    public void setB(double B) {
        this.vB = B;
    }

}
